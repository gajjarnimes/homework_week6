package homework_work6;

public class Class15 {
    public static void main(String[] args) {


        int x = 5;
        int y = 10;
        System.out.println("before swapping : x=" +" "+y  );
        x = x+y; // x=15;
        y = x-y; // y=15-10=5
        x = x-y; // x=15-5=10

        System.out.println("after swapping: x=" +" "+y);


    }
}
